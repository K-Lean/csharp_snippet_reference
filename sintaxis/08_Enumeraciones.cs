// Los enumeradores son utiles cuando una variable solo 
// puede tener un conjunto de valores concreto.

// Definicion de una enumeracion
enum Color { Rojo, Ver, Azul }

// Uso de una enumeracion

Color paleta = Color.Rojo;

// Visualizacion de una variable de enumeracion
Console.WriteLine("{0}", paleta); // Muestra el Rojo